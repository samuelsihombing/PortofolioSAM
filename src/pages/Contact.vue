<script setup>
import { ref } from 'vue'
import emailjs from '@emailjs/browser'

const form = ref({ name: '', email: '', message: '' })
const isSent = ref(false)
const isError = ref(false)
const isLoading = ref(false)

function submitForm() {
  isLoading.value = true
  isError.value = false

  emailjs.send(
    'service_akaq7nc',   // ← ganti dengan Service ID kamu
    'template_0t72ggg',  // ← ganti dengan Template ID kamu
    {
      from_name: form.value.name,
      from_email: form.value.email,
      message: form.value.message,
    },
    'IKdJqYMF9Lu5iMKxl'         // ← ganti dengan Public Key kamu
  )
  .then(() => {
    isSent.value = true
    isLoading.value = false
    form.value = { name: '', email: '', message: '' }

    setTimeout(() => {
      isSent.value = false
    }, 3000)
  })
  .catch(() => {
    isError.value = true
    isLoading.value = false
  })
}
</script>

